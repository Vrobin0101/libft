#include "libft.h"

int		ft_high_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0)
		return (0);
	while (i < 46400 && i <= nb)
	{
		if (i * i >= nb)
			return (i);
		i++;
	}
	return (i);
}

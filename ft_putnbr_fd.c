#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int nbr;

	if (n < 0)
	{
		n *= -1;
		ft_putchar_fd('-', fd);
	}
	nbr = n;
	if (nbr >= 10)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	else if (nbr <= 9)
	{
		ft_putchar_fd(nbr + '0', fd);
	}
}
